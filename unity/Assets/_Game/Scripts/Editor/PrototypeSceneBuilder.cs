    /// <summary>
    /// Builds and saves the Prototype scene entirely from code (AGENTS.md: scenes
    /// are generated by editor bootstrap code and are never the source of truth for
    /// logic). Layout follows docs/VERTICAL_SLICE_SPEC.md §2: shipwreck beach SW,
    /// ruined abbey hill NE (bell tower + chained hound), meadow camp center
    /// (campfire + storage), forest edge NW, a stream.
    ///
    /// P2-10 (final integration) fills the greybox into the FULL Phase 2 slice —
    /// wiring every system earlier tasks were forbidden from touching this file for:
    /// the salvage economy (SalvageSites on the wreck, a StoragePile that raises
    /// capacity, wreck-crate starting stock), villager jobs (Woodcutter/Guard
    /// JobWorkPoints and a JobAssigner applying the default roster), the four abbey
    /// RestorationNodes, the sacred abbey flame, the MorningReport system+panel, the
    /// GameSession win/loss authority with the FirstWhiteNightScenario arming the
    /// White Night on the director, the drowned-sailor shipwreck anchor, and the
    /// debug panels F2–F6 alongside the existing F1 overlay. Runtime systems never
    /// depend on the scene file (PlayMode tests build their own world), so this only
    /// assembles and cross-references components.
    ///
    /// Where a third-party prototype replacement exists under Assets/ThirdParty, it
    /// is preferred. Otherwise, where a generated Blender GLB is already imported
    /// under Assets/Generated/BlenderAssets it is instantiated; otherwise the
    /// builder degrades to primitives. The menu item never fails hard on missing art,
    /// and gameplay never blocks on it.
    /// </summary>
    public static class PrototypeSceneBuilder
    {
        public const string ScenePath = "Assets/_Game/Scenes/Prototype01.unity";
        public const string GeneratedAssetFolder = "Assets/Generated/BlenderAssets";
        public const string ThirdPartyAssetFolder = "Assets/ThirdParty";

        // Landmark positions (map layout, not balance — the map is authored here).
        static readonly Vector3 CampCenter = Vector3.zero;
        static readonly Vector3 BeachCenter = new Vector3(-30f, 0f, -30f);
        static readonly Vector3 AbbeyHillCenter = new Vector3(28f, 0f, 28f);
        static readonly Vector3 ForestEdgeCenter = new Vector3(-28f, 0f, 28f);
        static readonly Vector3 FarVillagerPost = new Vector3(-30f, 0f, 30f);

        [MenuItem("Tools/Abbey/Build Prototype Scene")]
        public static void BuildPrototypeScene()
        {
            var scene = EditorSceneManager.NewScene(NewSceneSetup.EmptyScene, NewSceneMode.Single);
            PopulateScene();

            try
            {
                string dir = Path.GetDirectoryName(Path.GetFullPath(
                    Path.Combine(Application.dataPath, "..", ScenePath)));
                if (!string.IsNullOrEmpty(dir))
                {
                    Directory.CreateDirectory(dir);
                }
                if (EditorSceneManager.SaveScene(scene, ScenePath))
                {
                    Debug.Log($"[Abbey] Prototype greybox scene built and saved to {ScenePath}.");
                }
                else
                {
                    Debug.LogWarning("[Abbey] Scene populated but SaveScene failed; " +
                                     "the open scene is still fully usable (press Play).");
                }
            }
            catch (Exception e)
            {
                Debug.LogWarning($"[Abbey] Scene populated but saving threw: {e.Message}. " +
                                 "The open scene is still fully usable (press Play).");
            }
        }

        /// <summary>Creates every world object in the active scene and wires the full slice.</summary>
        public static void PopulateScene()
        {
            var config = PrototypeConfig.LoadOrDefault();

            BuildGroundAndSun();
            var director = BuildSimulationCore(config);
            var hero = BuildHero();
            BuildCamera(config, hero.transform);
            BuildCamp(config);
            BuildSeedSlots();
            var abbeyFlame = BuildAbbeyHill(config);
            var wreckAnchor = BuildBeach();
            director.shipwreckAnchor = wreckAnchor;
            BuildForestEdgeAndStream();
            BuildVillagers();
            BuildJobsAndEconomy();
            BuildStarterProduction();
            BuildRestorationNodes();
            BuildSessionReportsAndPanels(hero, director, abbeyFlame);
        }

