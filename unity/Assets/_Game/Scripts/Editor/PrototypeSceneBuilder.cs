            PlaceRoad("Road_Camp_Forest", new Vector3(-4f, 0f, 4f),
                new Vector3(-6f, 0f, 8f), new Vector3(-17f, 0f, 17f), ForestEdgeCenter);
            PlaceTerrainAsset("FieldPlot", "field_plot_t1", new Vector3(-7f, 0.04f, 7f),
