        }

        static LightSource BuildAbbeyHill(PrototypeConfig config)
        {
            // Low greybox hill so the abbey reads as "up".
            var hill = GameObject.CreatePrimitive(PrimitiveType.Cylinder);
            hill.name = "AbbeyHill";
            hill.transform.position = AbbeyHillCenter + new Vector3(0f, 0.25f, 0f);
            hill.transform.localScale = new Vector3(12f, 0.25f, 12f);
            float hillTop = 0.5f;

            var towerPos = AbbeyHillCenter + new Vector3(0f, hillTop, 0f);
            InstantiateGenerated("bell_tower_ruined", towerPos, "BellTower",
                PrimitiveType.Cube, new Vector3(3f, 6f, 3f), 3f);
            InstantiateGenerated("abbey_wall_broken", towerPos + new Vector3(-4f, 0f, 1f),
                "AbbeyWall_A", PrimitiveType.Cube, new Vector3(4f, 1.5f, 0.5f), 0.75f);
            InstantiateGenerated("abbey_wall_broken", towerPos + new Vector3(1f, 0f, -4f),
                "AbbeyWall_B", PrimitiveType.Cube, new Vector3(0.5f, 1.5f, 4f), 0.75f);

            // Sacred abbey flame: win/loss anchor and the hound's home light.
            var flameGO = new GameObject("AbbeyFlame");
            flameGO.transform.position = towerPos + new Vector3(1.5f, 0f, 1.5f);
            var flame = flameGO.AddComponent<LightSource>();
            flame.radius = config.sacredFlameRadius;
            flame.strength = config.sacredFlameStrength;
            flame.sacred = true;
            flame.fuelSeconds = -1f; // 0.1: the sacred flame does not burn out

            // The chained Black Hound at the tower base.
            var chainPos = towerPos + new Vector3(2f, 0f, -1.5f);
            InstantiateGenerated("hound_chain", chainPos, "HoundChain",
                PrimitiveType.Cylinder, new Vector3(0.3f, 0.1f, 0.3f), 0.1f);
            var hound = InstantiateGenerated("black_hound_lowpoly",
                chainPos + new Vector3(0.8f, 0f, 0f), "BlackHound",
                PrimitiveType.Capsule, new Vector3(1.2f, 0.9f, 1.2f), 0.9f);
            hound.AddComponent<HoundController>();

            return flame;
        }

        static void BuildForestEdgeAndStream()
        {
            for (int i = 0; i < 5; i++)
            {
                var offset = new Vector3((i % 3) * 4f - 4f, 0f, (i / 3) * 5f);
                InstantiateGenerated("forest_tree_01", ForestEdgeCenter + offset,
                    $"ForestTree_{i}", PrimitiveType.Cylinder,
                    new Vector3(0.5f, 3f, 0.5f), 3f);
            }
            InstantiateGenerated("rock_cluster_01", ForestEdgeCenter + new Vector3(6f, 0f, -3f),
                "RockCluster", PrimitiveType.Sphere, new Vector3(1.5f, 1f, 1.5f), 0.4f);

            // Stream: a flat blue strip running along the forest edge toward the sea.
            var stream = GameObject.CreatePrimitive(PrimitiveType.Cube);
            stream.name = "Stream";
            stream.transform.position = new Vector3(-15f, 0.02f, 18f);
            stream.transform.localScale = new Vector3(45f, 0.05f, 2.5f);
            stream.transform.rotation = Quaternion.Euler(0f, -20f, 0f);
            var renderer = stream.GetComponent<Renderer>();
            if (renderer != null && renderer.sharedMaterial != null)
            {
                var mat = new Material(renderer.sharedMaterial) { color = new Color(0.25f, 0.45f, 0.7f) };
                renderer.sharedMaterial = mat;
            }
        }

        static void BuildVillagers()
        {
            // 11 villagers around camp; positions fan out inside/near the fire.
            for (int i = 0; i < 11; i++)
            {
                float angle = i / 11f * Mathf.PI * 2f;
                float ringRadius = 3f + (i % 3);
                var pos = CampCenter + new Vector3(
                    Mathf.Cos(angle) * ringRadius, 0f, Mathf.Sin(angle) * ringRadius);
                CreateVillager($"Villager_{i:D2}", pos, seed: i);
            }

            // The 12th villager works the far forest edge: the dusk drama beat.
            CreateVillager("Villager_FarNW", FarVillagerPost, seed: 11);
        }

