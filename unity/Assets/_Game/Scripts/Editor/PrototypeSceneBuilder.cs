        /// <summary>
        /// Wires the run-level systems: the MorningReport system+panel (dawn
        /// consequence report), the GameSession win/loss authority (referencing the
        /// hero and the sacred abbey flame), the FirstWhiteNightScenario (arming the
        /// director for the White Night), the GameOutcome end screen, and the debug
        /// panels F2–F6 alongside the existing F1 DebugOverlay.
        /// </summary>
        static void BuildSessionReportsAndPanels(
            GameObject hero, NightmareDirector director, LightSource abbeyFlame)
        {
            var bellkeeper = hero.GetComponent<BellkeeperController>();

            // Morning consequence report (system + player-facing panel, F5).
            var reportsGO = new GameObject("MorningReportSystem");
            reportsGO.AddComponent<MorningReportSystem>();
            reportsGO.AddComponent<MorningReportPanel>();

            // The scripted climax: arms the director's harder schedule at the dusk
            // before the White Night.
            var scenarioGO = new GameObject("FirstWhiteNightScenario");
            var scenario = scenarioGO.AddComponent<FirstWhiteNightScenario>();
            scenario.director = director;

            // The outcome authority + end screen (F6).
            var sessionGO = new GameObject("GameSession");
            var session = sessionGO.AddComponent<GameSession>();
