        /// <summary>
        /// The Phase 2 logistics layer (replaces the 0.1 VillagerWorkAssigner):
        /// Woodcutter JobWorkPoints at the forest edge, Guard JobWorkPoints in camp,
        /// a <see cref="JobAssigner"/> that applies the data-driven default roster to
        /// the registered villagers on Start, and the <see cref="SettlementEconomyBootstrap"/>
        /// that grants the wreck-crate starting stock once storage capacity exists.
        /// Salvagers find the wreck <see cref="SalvageSite"/>s, Builders serve the
        /// abbey <see cref="RestorationNode"/>s, Tenders feed the lights — all via
