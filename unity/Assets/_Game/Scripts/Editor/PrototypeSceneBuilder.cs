        static void BuildCamera(PrototypeConfig config, Transform followTarget)
        {
            var camGO = new GameObject("IsoCamera");
            camGO.tag = "MainCamera";
            var cam = camGO.AddComponent<Camera>();
            cam.orthographic = true;
            cam.orthographicSize = config.cameraDefaultOrthoSize;
            camGO.transform.rotation = Quaternion.Euler(
                IsoCameraController.Pitch, IsoCameraController.Yaw, 0f);
            var rig = camGO.AddComponent<IsoCameraController>();
            rig.SetWorldBounds(new Vector2(-50f, -50f), new Vector2(50f, 50f));
            rig.FocusOn(CampCenter);
            rig.SetFollowTarget(followTarget);
        }

        static void BuildCamp(PrototypeConfig config)
        {
            // Campfire: the first light zone. GLB when imported, cylinder otherwise.
            var campfire = InstantiateGenerated("campfire_t1", CampCenter, "Campfire",
                PrimitiveType.Cylinder, new Vector3(0.8f, 0.25f, 0.8f), 0.25f);
            TagPrototypeBuilding(campfire, "campfire_t1");
            var fireLight = campfire.AddComponent<LightSource>();
            fireLight.radius = config.campfireRadius;
            fireLight.strength = config.campfireStrength;
            fireLight.fuelSeconds = config.defaultFuelSeconds;
            fireLight.fuelConsumptionPerSecond = config.fuelConsumptionPerSecond;

            // Storage pile: raises the ledger's capacity (StoragePile registers in
            // OnEnable). The wreck-crate starting stock is granted at play-start by
            // SettlementEconomyBootstrap, once this pile's capacity is counted.
            var storage = InstantiateGenerated("storage_pile_t1", new Vector3(3f, 0f, 3f),
                "StoragePile", PrimitiveType.Cube, new Vector3(1.2f, 0.6f, 1.2f), 0.3f);
            TagPrototypeBuilding(storage, "storage_pile_t1");
            storage.AddComponent<StoragePile>();

            // The everyday water source now has a coherent off-the-shelf CC0 model,
            // matching the threat source registered near this side of camp.
            InstantiateGenerated("well_t1", new Vector3(5f, 0f, 4.5f), "CampWell",
                PrimitiveType.Cylinder, new Vector3(1.8f, 1.4f, 1.8f), 0.7f);

            // Shelters are destructible homes (P3-05): they carry a Building bound to
            // the shelter_t1 catalog entry so HomeDefenseSystem can wake, flare and
            // (if overwhelmed) raze them. Occupants are assigned at play-start by the
            // SettlerHomeBootstrap through the SanitySystem shelter map.
            MakeHome(new Vector3(-4f, 0f, 2f), "Shelter_A");
            MakeHome(new Vector3(-2f, 0f, 5f), "Shelter_B");

            // Lantern post on the camp's dark side, toward the abbey path.
            var lantern = InstantiateGenerated("lantern_post_t1", new Vector3(10f, 0f, 6f),
                "LanternPost", PrimitiveType.Cube, new Vector3(0.3f, 2f, 0.3f), 1f);
            TagPrototypeBuilding(lantern, "lantern_post_t1");
            var lanternLight = lantern.AddComponent<LightSource>();
            lanternLight.radius = config.lanternRadius;
            lanternLight.strength = config.lanternStrength;
            lanternLight.fuelSeconds = config.defaultFuelSeconds;
            lanternLight.fuelConsumptionPerSecond = config.fuelConsumptionPerSecond;

            // Warrior tier (P3-06): a lodge on the dark edge of camp promotes settlers
            // into Dark-capable warriors (recruit via the F6 combat panel / debug tools)
            // and a watchtower gives ranged support + the vision that arms the nightly
            // dark objective. Both carry a Building bound to their catalog entry so the
            // WarriorStructure component (attached by Building.Construct) drives them.
            MakeWarriorStructure("warrior_lodge_t1", new Vector3(-8f, 0f, 6f), "WarriorLodge",
